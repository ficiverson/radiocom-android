package justforcommunity.radiocom.adapters;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.ArrayList;
import java.util.List;

// Class to control number of page
public class PageAdapter extends FragmentPagerAdapter {

    private List<DayPage> mTabs;

    public PageAdapter(FragmentManager fm, List<DayPage> mTabs) {
        super(fm);
        this.mTabs = mTabs;
    }

    @Override
    public Fragment getItem(int i) {
        return mTabs.get(i).createFragment(mTabs.get(i).getDate());
    }

    @Override
    public int getCount() {
        return mTabs.size();
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return mTabs.get(position).getTitle();
    }

}